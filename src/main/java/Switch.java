public class Switch {
    public static void main(String[] args) {
        switch (2){
            case 2:
                System.out.println("12");
                break;     case 3:
                System.out.println("13");
                break;     case 5:
                System.out.println("15");
                break;     case 6:
                System.out.println("16");
                break;     case 8:
                System.out.println("8");
                break;
            default:System.out.println("Ни одно не подошло");
        }
    }
}
